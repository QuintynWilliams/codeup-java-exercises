public class StringExercise {
    public static void main(String[] args) {

        String ctrl =   "We don't need no education\n" +
                        "We don't need no thought control\n";
        System.out.println(ctrl);

        String mark =   "Check \"this\" out!, \"s inside of \"s\n";
        System.out.println(mark);

        String cmd =    "In windows, the main drive is usually C:\\\n";
        System.out.println(cmd);

        String back =   "I can do backslashes \\, double backslashes \\\\,\n" +
                        "and the amazing triple backslash \\\\\\!\n";
        System.out.println(back);

    }
}
